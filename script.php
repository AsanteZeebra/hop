$(function() {
  // Dropzone class
  var myDropzone = new Dropzone(".dropzone", {
    url: "save_photo.php",
    paramName: "file",
    maxFilesize: 4, // Max filesize in MB
    addRemoveLinks: true,
    maxFiles: 10,
    params: { memid: $('.tfid').val() }, // Pass additional parameters
    acceptedFiles: ".jpeg,.jpg,.png",
    autoProcessQueue: false, // Prevent automatic uploads

    // Show progress bar when file is added
    init: function() {
      var progressBar = $('#progressBar');
      var progressWrapper = $('#progressWrapper');

      this.on("totaluploadprogress", function(progress) {
        progressWrapper.show();
        progressBar.css('width', progress + '%');
        progressBar.text(Math.round(progress) + '%');
      });

      this.on("queuecomplete", function() {
        progressWrapper.hide();
        progressBar.css('width', '0%');
        progressBar.text('0%');
      });

      this.on("error", function(file, response) {
        progressWrapper.hide();
        progressBar.css('width', '0%');
        progressBar.text('0%');
      });

      // Refresh page after all files are uploaded successfully
      this.on("successmultiple", function(files, response) {
        // Here you can put any logic you want after successful upload
        // For example, showing a success message, etc.
        setTimeout(function() {
          location.reload(); // Refresh the page
        }, 2000); // Adjust the delay if necessary
      });
    }
  });

  $('#startUpload').click(function() {
    myDropzone.processQueue(); // Start upload
  });
});


  
